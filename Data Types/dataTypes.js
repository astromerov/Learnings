// 4. Undefined - represents an uninitialized variable.
let undefinedVar;

// 5. Null - represents an intentional absence of any value or object.
let nullVariable = null;

// 6. Symbol - represents a unique immutable value, often used as object property keys.
const uniqueSymbol = Symbol("description");

// 7. Object - represents a collection of key-value pairs.
let person = {
    person: "Human",
    name: "John",
    lastName: "Doe",
    age: 30
};

// 8. Array - represents list-like collection of vlaue.
