/*

In JavaScript, you can declare variables in three ways.
var - older way of declaring. It's function scoped, which means its available within the function where its declared
let - Introduced in modern JavaScript. It's block-scoped, meaning it's confined to the block (enclosed by curly braces) where it's defined
const - Also introduced in modern JavaScript. It's block-scoped, and its value cannot be changed once assigned.

*/

var olderVariable = "This is an older way of storing a variable";
let modernVariable = "This is modern way of storing a variable";
const anotherModernVariable = "This is also modern way of storing, however, it cannot be changed once assigned";

